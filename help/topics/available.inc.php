<p>To prevent the site, section, or page from being viewed at all, select "Hide". 
This is useful for preventing people from viewing the site while it is still in progress. 

<p>Selecting "Hide" WILL OVER-RIDE the "Site available" date. 
Selecting "Don't Hide" will not over-ride the "site unavailable" settings.

<p>Selecting "Hide" will over-ride all view permissions.
If "Hide" is selected, only the owner and people with editing (add/edit/delete) permissions will be able to view the site.
People with editing permissions will only be able to view those parts of the site to which they have permission.