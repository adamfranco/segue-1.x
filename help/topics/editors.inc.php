<p>The ability to assign editors and permissions to your site is one of the most powerful features of SitesDB.

<p>Any editors you add will see your site under their sites list. This, however, does not mean that they can edit your site, at least not yet. As site owner, you can specify which editors can do what to your site, on any level.

<p>To add an editor to your site, click the "add editor" link. You will be presented with a dialog that allows you to search for Middlebury College users and add them as editors.

<p>Once they are editors you can specify what they can do to your site. Permissions are organized into "add", "edit", and "delete", and can be set on the site, section or page level. By checking boxes on this page you give the editors permissions over your entire site.

<p><b>Add</b> permissions allow people to add content, <b>Edit</b> permissions allow them to edit existing content and <b>Delete</b> permissions allow them to delete existing content.