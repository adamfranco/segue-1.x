<p>Segue allows you to specify date ranges to make content available to end users. This can be set on any level of your website.

<p>To make your website available to end users during a certain range of dates, check the boxes next to "Activate date" and/or "Deactivate date" and enter the applicable dates.

<p>Activation dates and the "Hide" option override view permissions. This means that if this content is not in an active date range or if it is hidden, only the site owner and people who have editing permissions can view it.
